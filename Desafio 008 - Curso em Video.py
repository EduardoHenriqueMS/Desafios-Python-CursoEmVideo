# Escreva um programa que leia um valor em metros e o exiba
# convertido em centímetros e milímetros.

metros = float(input('Escreva o valor em metros do objeto ou da distância a ser percorrida:'))

centi = metros * 100
mili = metros * 1000

print('O valor em centímetros do objeto ou da distância a ser percorrida é: {}'.format(centi))
print('O valor em milímetros do objeto ou da distância a ser percorrida é: {}'.format(mili))



