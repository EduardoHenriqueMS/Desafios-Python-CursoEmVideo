# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

valor1 = int(input('Digite um número:'))

dobro = valor1 * 2
triplo = valor1 * 3
raiz_quadrada = valor1 ** (1/2)

print('O dobro do número {} é: {}'.format(valor1, dobro))
print('O triplo do número {} é: {}'.format(valor1, triplo))
print('A raiz quadrada do número {} é: {}'.format(valor1 , raiz_quadrada))

